---@class LDNodeEditorStreamingUnloadSpecialCharacterNode : LDNodeEditorNode
---@field slotId integer

local STREAMING_UNLOAD_SPECIAL_CHARACTER_NODE_TYPE = "core.streaming_unload_special_character"

---@type LDNodeEditorNodeType
local streamingUnloadSpecialCharacterNode = {
    typeName = STREAMING_UNLOAD_SPECIAL_CHARACTER_NODE_TYPE,
    category = "opcode.streaming",
    icon = nodesIcons["function"],
    color = nodesColors["function"],
	description = true,
    isCallable = true,
    ---@param ctx LDNodeEditorContext
    ---@param newNodeId integer
    ---@param getPinId fun():integer
    new = function(ctx, newNodeId, getPinId)
        ---@type LDNodeEditorStreamingUnloadSpecialCharacterNode
        local newNode = {
            id = newNodeId,
            nodeType = STREAMING_UNLOAD_SPECIAL_CHARACTER_NODE_TYPE,
            inputs = {
                {
                    id = getPinId(),
                    node = newNodeId,
                    kind = NodeEditorPinKind.Input,
                    type = "core.flow",
                }
				,{
                    id = getPinId(),
                    node = newNodeId,
                    kind = NodeEditorPinKind.Input,
                    type = "core.number",
                }

            },
            outputs = {
                {
                    id = getPinId(),
                    node = newNodeId,
                    kind = NodeEditorPinKind.Output,
                    type = "core.flow",
                }

            },
			slotId = 0
        }
        return newNode
    end,
    ---@param editor LDNodeEditor
    ---@param ctx LDNodeEditorContext
    ---@param node LDNodeEditorStreamingUnloadSpecialCharacterNode
    ---@param builder BlueprintNodeBuilder
    draw = function(editor, ctx, node, builder)
        local fontScale = ImGui.GetFontSize() / 16;

        builder:Begin(NodeEditor.NodeId(node.id));
        LDNodeEditor.defaultHeader(editor, builder, node);

        LDNodeEditor.defaultInput(editor, ctx, builder, node.inputs[1], "");
		LDNodeEditor.defaultInput(editor, ctx, builder, node.inputs[2], "slotId", function ()
            node.slotId = editor.dataTypes[node.inputs[2].type].drawEditValue(node.slotId, "##slotIdEdit", fontScale * 100)
        end);

		LDNodeEditor.defaultOutput(editor, ctx, builder, node.outputs[1], "");


        builder:End();
    end,
    ---@param editor LDNodeEditor
    ---@param context LDNodeEditorContext
    ---@param node LDNodeEditorStreamingUnloadSpecialCharacterNode
    ---@param inputValues any[]
    run = function(editor, context, node, inputValues)
        local slotId = inputValues[2] or node.slotId
		StreamingOp.unloadSpecialCharacter(slotId)
        return {1}
    end
}

return streamingUnloadSpecialCharacterNode
